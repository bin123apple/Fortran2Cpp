MODULE MYGA_PARAMS
   IMPLICIT NONE
   INTEGER, PARAMETER :: dp = KIND(1.0D0)
   INTEGER :: MYGA_NSTRUC = 0, MYGA_NOFF = 0
   INTEGER :: MYGA_NMUT=0
   INTEGER :: MYGA_GENS = 0
   INTEGER :: MYGA_TOURN_SIZE = 3
   LOGICAL :: MYGA_L_EPOCH=.FALSE.
   INTEGER :: MYGA_EPOCH_SAVE = 0
   LOGICAL :: MYGA_EPOCH_DUMP = .FALSE.
   INTEGER :: MYGA_COUNT_EPOCH = 1
   REAL(KIND=dp) :: MYGA_MUT_RATE=0.1_dp
   INTEGER :: MYGA_CROSS = 1
   REAL(KIND=dp) :: MYGA_BQMAX = 0.0_dp, MYGA_CQMAX = 0.0_dp
   REAL(KIND=dp) :: MYGA_DUPLICATE_ETHRESH=0.0_dp
   REAL(KIND=dp) :: MYGA_DUPLICATE_GTHRESH=0.174532925_dp
   REAL(KIND=dp) :: MYGA_EPOCH_THRESH=0.01_dp
   REAL(KIND=dp) :: MYGA_LAST_ENERGY=1D10
   LOGICAL :: MYGA_L_ROUL=.FALSE.
   LOGICAL :: MYGA_L_CHAIN=.FALSE.
   LOGICAL :: MYGA_L_SPHERE=.FALSE.
   REAL(KIND=dp) :: MYGA_SPHERE_RADIUS=3.0_dp
   INTEGER :: MYGA_COUNT_MIN = 0
   INTEGER :: CURR_GEN=0
   REAL(KIND=dp) :: MYGA_BH_INIT = 0.0_dp
   REAL(KIND=dp) :: MYGA_BH_INCR=0.0_dp
   REAL(KIND=dp) :: MYGA_BH_STEPS = 0.0_dp
   LOGICAL :: MYGA_DUMP_POP=.FALSE.
   LOGICAL :: MYGA_TWIN=.FALSE.
END MODULE MYGA_PARAMS

PROGRAM test_myga_params
  USE MYGA_PARAMS
  IMPLICIT NONE

  WRITE(*,*) 'Testing MYGA_PARAMS Module:'
  WRITE(*,*) 'MYGA_TOURN_SIZE should be 3. Value:', MYGA_TOURN_SIZE
  WRITE(*,*) 'MYGA_L_EPOCH should be .FALSE. Value:', MYGA_L_EPOCH
  WRITE(*,*) 'MYGA_MUT_RATE should be 0.1. Value:', MYGA_MUT_RATE

  IF (MYGA_TOURN_SIZE == 3) THEN
    WRITE(*,*) 'Test MYGA_TOURN_SIZE Passed'
  ELSE
    WRITE(*,*) 'Test MYGA_TOURN_SIZE Failed'
  END IF

  IF (.NOT. MYGA_L_EPOCH) THEN
    WRITE(*,*) 'Test MYGA_L_EPOCH Passed'
  ELSE
    WRITE(*,*) 'Test MYGA_L_EPOCH Failed'
  END IF

  IF (MYGA_MUT_RATE == 0.1_dp) THEN
    WRITE(*,*) 'Test MYGA_MUT_RATE Passed'
  ELSE
    WRITE(*,*) 'Test MYGA_MUT_RATE Failed'
  END IF

END PROGRAM test_myga_params
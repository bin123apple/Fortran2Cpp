PROGRAM TestEPJ2JD
  IMPLICIT NONE
  DOUBLE PRECISION EPJ, DJM0, DJM

  ! Test data: Known epoch year and expected results
  EPJ = 2000.0
  CALL iau_EPJ2JD(EPJ, DJM0, DJM)

  PRINT *, "Testing EPJ2JD with EPJ =", EPJ
  PRINT *, "Expected DJM0: 2400000.5, DJM: 51544.5"
  PRINT *, "Computed DJM0:", DJM0, "DJM:", DJM

END PROGRAM TestEPJ2JD

SUBROUTINE iau_EPJ2JD ( EPJ, DJM0, DJM )
  IMPLICIT NONE
  DOUBLE PRECISION EPJ, DJM0, DJM

  DJM0 = 2400000.5D0
  DJM  = 51544.5D0 + ( EPJ-2000D0 ) * 365.25D0
END
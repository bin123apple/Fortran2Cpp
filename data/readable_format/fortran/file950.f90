PROGRAM testXLAENV
    INTEGER IPARMS(100)
    COMMON /CLAENV/ IPARMS
    EXTERNAL XLAENV

    CALL XLAENV(1, 10)
    CALL XLAENV(5, 50)
    CALL XLAENV(9, 90)

    PRINT *, 'Testing XLAENV...'
    PRINT *, 'IPARMS(1):', IPARMS(1)
    PRINT *, 'IPARMS(5):', IPARMS(5)
    PRINT *, 'IPARMS(9):', IPARMS(9)
END PROGRAM testXLAENV

SUBROUTINE XLAENV(ISPEC, NVALUE)
    INTEGER ISPEC, NVALUE
    INTEGER IPARMS(100)
    COMMON /CLAENV/ IPARMS
    IF (ISPEC.GE.1 .AND. ISPEC.LE.9) THEN
        IPARMS(ISPEC) = NVALUE
    END IF
    RETURN
END SUBROUTINE XLAENV